import UIKit


struct Font {

    static func titilliumWebRegular(size: CGFloat) -> UIFont {
      return UIFont(name: "TitilliumWeb-Regular", size: size)!
    }

    static func titilliumWebLight(size: CGFloat) -> UIFont {
        return UIFont(name: "TitilliumWeb-Light", size: size)!
    }
    
    static func titilliumWebSemiBold(size: CGFloat) -> UIFont {
        return UIFont(name: "TitilliumWeb-SemiBold", size: size)!
    }
    
}
